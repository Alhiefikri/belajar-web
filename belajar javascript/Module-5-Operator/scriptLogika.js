/* Operator Logika */

// And && Operator (Harus kedua nilai true jika mau nilai true)
let a = false;
let b = true;
let result = a && b;

console.log(result);

// Or Operator || (jika salah satunya ada yang true maka akan bernilai true)
let c = true;
let d = false;
let orResult = c || d;

console.log(orResult);

// not operator ! (kebalikan dari nilai)
let e = true;
let notResult = !e;
console.log(notResult);
